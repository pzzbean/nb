	public static String getLanguage(HttpServletRequest request) {
		String lang = "en";
		Cookie[] cooks = request.getCookies();
        for (Cookie cookie : cooks) {
			if ("userLanguage".equalsIgnoreCase(cookie.getName())) {
				lang= cookie.getValue();
			}
		}
        return lang;
	}
	
	public static Map<String, String> getLangMap(String lang) {
		Map<String, String> map =null;
		if("En".equalsIgnoreCase(lang)) {
			map = I18nUtil.getEnMap();
		}else if("zh-CN".equalsIgnoreCase(lang)){
			map = I18nUtil.getZhMap();
		}
		return map;
	}
